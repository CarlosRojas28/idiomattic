// Function to write a PO file
function write_po_file($dir, $locale_data) {
    $locale = $locale_data['language'];
    $language_team = $locale_data['language_team'];
    $plural_forms = $locale_data['plural_forms'];
    $translations = $locale_data['translations'];
    $plurals = $locale_data['plurals'] ?? [];

    $content = "# {$language_team} translation for Idiomattic WP\n";
    $content .= "# Copyright (C) 2024 Idiomattic\n";
    $content .= "msgid \"\"\n";
    $content .= "msgstr \"\"\n";
    $content .= "\"Project-Id-Version: Idiomattic WP\\n\"\n";
    $content .= "\"POT-Creation-Date: 2024-01-01 00:00+0000\\n\"\n";
    $content .= "\"PO-Revision-Date: 2024-01-01 00:00+0000\\n\"\n";
    $content .= "\"Last-Translator: Idiomattic <hello@idiomattic.app>\\n\"\n";
    $content .= "\"Language-Team: {$language_team}\\n\"\n";
    $content .= "\"Language: {$locale}\\n\"\n";
    $content .= "\"MIME-Version: 1.0\\n\"\n";
    $content .= "\"Content-Type: text/plain; charset=UTF-8\\n\"\n";
    $content .= "\"Content-Transfer-Encoding: 8bit\\n\"\n";
    $content .= "\"Plural-Forms: {$plural_forms}\\n\"\n";
    $content .= "\"X-Generator: Idiomattic WP\\n\"\n";
    $content .= "\"X-Domain: idiomattic-wp\\n\"\n\n";

    foreach ($translations as $msgid => $msgstr) {
        $escaped_msgid = str_replace('"', '\\"', $msgid);
        $escaped_msgstr = str_replace('"', '\\"', $msgstr);
        $content .= "msgid \"{$escaped_msgid}\"\n";
        $content .= "msgstr \"{$escaped_msgstr}\"\n\n";
    }

    // Plural forms
    foreach ($plurals as $msgid => $msgstrs) {
        $escaped_msgid = str_replace('"', '\\"', $msgid);
        $content .= "msgid \"{$escaped_msgid}\"\n";
        $content .= "msgid_plural \"{$escaped_msgid}\"\n";
        foreach ($msgstrs as $i => $msgstr) {
            $escaped_msgstr = str_replace('"', '\\"', $msgstr);
            $content .= "msgstr[{$i}] \"{$escaped_msgstr}\"\n";
        }
        $content .= "\n";
    }

    $filename = "{$dir}/idiomattic-wp-{$locale}.po";
    file_put_contents($filename, $content);
    echo "Written: {$filename}\n";
}

foreach ($locales as $locale => $data) {
    write_po_file($dir, $data);
}

echo "Done writing PO files.\n";
